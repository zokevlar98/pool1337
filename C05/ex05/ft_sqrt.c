int	ft_sqrt(int nb)
{
	long	x;
	long	n;

	x = 0;
	n = nb;
	if (n < 0)
	{
		return (0);
	}
	while (x * x < n)
	{
		x++;
	}
	if ((x * x) == n)
	{
		return (x);
	}
	else
	{
		return (0);
	}
}
