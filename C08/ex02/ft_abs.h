#ifndef FT_ABS_H
# define FT_ABS_H

# define ABS(Value)						\
({										\
 	__typeof__(Value) y = (Value);		\
	if (y < 0)							\
	{									\
		y = -y;							\
	}									\
	y;									\
})

#endif
